export function toWords(words,indexAlphabet = 0){
    let strArray = []
    for (let s of words){
        if(/[A-Z]/.test(s)){
            strArray.push(alphabet[indexAlphabet][s.charCodeAt(0)-65])
        }else if(/[a-z]/.test(s)){
            strArray.push(alphabet[indexAlphabet][s.charCodeAt(0)-71])
        }else{
            strArray.push(s)
        }
    }
    const finalWords = strArray.join('')
    return finalWords
}

